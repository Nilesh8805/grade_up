    public void showinfo(){
        ArrayList<User> list=userList();
        DefaultTableModel model= (DefaultTableModel)jdashboardtable.getModel();
        model.setRowCount(0);
        Object[] row =new Object[6];
        for(int i=0;i<list.size();i++){
            
            row[0]=list.get(i).getname();
            row[1]=list.get(i).getclas();
            row[2]=list.get(i).getmax();
            row[3]=list.get(i).getobtain();
            float obtper= list.get(i).getobtain();
            float maxper= list.get(i).getmax();
            float f;
            if(obtper==0){
                f=0;
                
            }else{
                float temp= (obtper/maxper)*100;
            
                DecimalFormat df=new DecimalFormat("#.##");
                f=Float.valueOf(df.format(temp));
            }
            row[4]=f;
            
            String grade="";
            if(f>=90){
                grade="A++";
            }
            if(80<=f && f<90){
                grade="A";
            }
            if(70<=f && f<80){
                grade="B";
            }
            if(60<=f && f<70){
                grade="C";
            }
            if(50<=f && f<60){
                grade="D";
            }
            if(40<=f && f<50){
                grade="E";
            }
            if(40>f){
                grade="Fail";
            }
            
            row[5]= grade;
            
            model.addRow(row);
           
        }
    }
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
